package DataStructures;

public class Triple {

	private double dij, base, greedy;

	public double getDij() {
		return dij;
	}

	public void setDij(double dij) {
		this.dij = dij;
	}

	public double getBase() {
		return base;
	}

	public void setBase(double base) {
		this.base = base;
	}

	public double getGreedy() {
		return greedy;
	}

	public void setGreedy(double greedy) {
		this.greedy = greedy;
	}

	public Triple(double dij, double base, double greedy) {
		super();
		this.dij = dij;
		this.base = base;
		this.greedy = greedy;
	}

	
}
